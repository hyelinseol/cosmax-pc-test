var draggablePlus02 = new Swiper('.select [class*="plus"] .slide-draggable02',{
    initialSlide: 1,
    direction: 'horizontal',
    loop: false,
    allowTouchMove: false,
    effect: 'fade',
    fadeEffect: {
        crossFade: true
    },
    scrollbar: {
        el: '.select [class*="plus"] .control_bar02 .bar',
        hide: false,
        draggable: true,
    },
    on: {
        slideChange: function(){
            $('.select [class*="plus"] .control_bar02 ol li').removeClass('checked').eq(draggablePlus02.activeIndex).addClass('checked');
        }
    }
});
$('.select [class*="plus"] .control_bar02 ol li').on('click', function(){
    draggablePlus02.slideTo($(this).index());
});

